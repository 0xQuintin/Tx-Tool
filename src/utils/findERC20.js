export let getBalance = async (_tokenAddress) => {
    let tokenAddress = _tokenAddress;
    let contract = new web3.eth.Contract(ERC20ABI, tokenAddress);
    let balance = await contract.methods.balanceOf('0x1D2B445D3E872c3d0F8D458551f0025Bf14AA5A3').call();
    let name = await contract.methods.name().call();
    return [balance, name];
}

